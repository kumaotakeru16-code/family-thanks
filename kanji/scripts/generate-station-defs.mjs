import fs from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()

const MIDDLE_AREA_JSON = path.join(ROOT, 'data/hotpepper-middle-area.json')
const SMALL_AREA_JSON = path.join(ROOT, 'data/hotpepper-small-area.json')
const OUTPUT_TS = path.join(ROOT, 'app/lib/station-defs.ts')

const MANUAL_ALIASES = {
  札幌: ['札幌', 'さっぽろ'],
  梅田: ['梅田', '大阪梅田', '東梅田'],
  難波: ['難波', 'なんば', '大阪難波'],
  三宮: ['三宮', '神戸三宮'],
  天神: ['天神', '西鉄福岡'],
  秋葉原: ['秋葉原', '秋葉原駅'],
  静岡: ['静岡'],
}

const MANUAL_HP_AREA = {
  横浜: { middleArea: 'Y135', smallArea: 'X270' },
  札幌: { middleArea: 'Y505', smallArea: 'X501' },
  大阪: { middleArea: 'Y300', smallArea: 'X300' },
  梅田: { middleArea: 'Y300', smallArea: 'X300' },
  品川: { middleArea: 'Y025', smallArea: 'X075' },
  静岡: { middleArea: 'Y230', smallArea: 'X495' },
}

function readJson(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, '').trim()
  let parsed = JSON.parse(raw)
  if (typeof parsed === 'string') parsed = JSON.parse(parsed)
  return parsed
}

function isStationLike(name) {
  const n = name.trim()

  // NG（エリア/観光/広域）
  const ngPatterns = [
    /その他/,
    /中心部/,
    /周辺/,
    /エリア/,
    /一帯/,
    /通り/,
    /横丁/,
    /市$/,
    /町$/,
    /村$/,
    /区$/,
    /郊外/,
    /ベイ/,
    /ランド/,
  ]

  if (ngPatterns.some((re) => re.test(n))) return false

  // OK条件
  const okPatterns = [
    /駅/,
    /^[一-龥ぁ-んァ-ン]{2,6}$/, // 日本語短名（新宿 渋谷 浜松など）
  ]

  return okPatterns.some((re) => re.test(n))
}

function normalize(text) {
  return String(text ?? '')
    .trim()
    .replace(/\s+/g, '')
    .replace(/駅$/, '')
}

function uniq(arr) {
  return [...new Set(arr)]
}

function safeArray(value) {
  return Array.isArray(value) ? value : []
}

function extractMiddleAreas(json) {
  return json?.results?.middle_area || json?.middle_area || []
}

function extractSmallAreas(json) {
  return json?.results?.small_area || json?.small_area || []
}

function getName(obj) {
  return obj?.name ?? ''
}

function getCode(obj) {
  return obj?.code ?? ''
}

function getMiddleCodeFromSmall(obj) {
  return obj?.middle_area?.code || ''
}

/**
 * 完全全国向け:
 * small_area から canonical 候補を全部作る。
 * ただし「県その他」「市その他」など雑なエリアは除外。
 */
function isUsableSmallAreaName(name) {
  const n = String(name ?? '').trim()
  if (!n) return false

  const ngPatterns = [
    /その他/,
    /県南部/,
    /県北部/,
    /県内/,
    /市外/,
    /郊外/,
    /近郊/,
    /周辺/,
    /一円/,
    /エリア/,
    /全域/,
  ]

  if (ngPatterns.some((re) => re.test(n))) return false

  return true
}

/**
 * canonical を作る
 * - 原則 small_area.name を使う
 * - 末尾の「駅」は落とす
 */
function canonicalizeSmallAreaName(name) {
  return normalize(name)
}

function buildMiddleAreaMap(middleAreas) {
  const map = new Map()
  for (const area of middleAreas) {
    const code = getCode(area)
    if (!code) continue
    map.set(code, area)
  }
  return map
}

function buildDefsFromSmallAreas(middleAreaMap, smallAreas) {
  const defsMap = new Map()

  for (const area of smallAreas) {
    const rawName = getName(area)
    if (!isStationLike(rawName)) continue

    const canonical = canonicalizeSmallAreaName(rawName)
    const smallArea = getCode(area)
    const middleArea = getMiddleCodeFromSmall(area)

    if (!canonical || !smallArea || !middleArea || !middleAreaMap.has(middleArea)) {
      continue
    }

    if (!defsMap.has(canonical)) {
      defsMap.set(canonical, {
        canonical,
        displayName: canonical,
        aliases: uniq([canonical, ...(MANUAL_ALIASES[canonical] ?? [])]),
        hpArea: {
          middleArea,
          smallArea,
        },
        source: 'master',
      })
    }
  }

  return defsMap
}

function applyManualOverrides(defsMap) {
  for (const [station, hpArea] of Object.entries(MANUAL_HP_AREA)) {
    defsMap.set(station, {
      canonical: station,
      displayName: station,
      aliases: uniq([station, ...(MANUAL_ALIASES[station] ?? [])]),
      hpArea,
      source: 'manual',
    })
  }

  for (const [station, aliases] of Object.entries(MANUAL_ALIASES)) {
    const existing = defsMap.get(station)
    if (existing) {
      existing.aliases = uniq([station, ...aliases])
      defsMap.set(station, existing)
    } else {
      defsMap.set(station, {
        canonical: station,
        displayName: station,
        aliases: uniq([station, ...aliases]),
        source: 'fallback',
      })
    }
  }

  return defsMap
}

function toTsObject(defs) {
  const lines = []

  lines.push(`// app/lib/station-defs.ts`)
  lines.push(`// AUTO-GENERATED FILE. DO NOT EDIT DIRECTLY.`)
  lines.push(`// Generated by scripts/generate-station-defs.mjs`)
  lines.push(``)
  lines.push(`export type HpArea = {`)
  lines.push(`  middleArea?: string`)
  lines.push(`  smallArea?: string`)
  lines.push(`}`)
  lines.push(``)
  lines.push(`export type StationDef = {`)
  lines.push(`  canonical: string`)
  lines.push(`  displayName: string`)
  lines.push(`  aliases: string[]`)
  lines.push(`  hpArea?: HpArea`)
  lines.push(`}`)
  lines.push(``)
  lines.push(`export const STATION_DEFS: Record<string, StationDef> = {`)

  for (const def of defs) {
    lines.push(`  ${JSON.stringify(def.canonical)}: {`)
    lines.push(`    canonical: ${JSON.stringify(def.canonical)},`)
    lines.push(`    displayName: ${JSON.stringify(def.displayName)},`)
    lines.push(`    aliases: ${JSON.stringify(def.aliases)},`)
    if (def.hpArea?.middleArea || def.hpArea?.smallArea) {
      lines.push(`    hpArea: {`)
      if (def.hpArea.middleArea) {
        lines.push(`      middleArea: ${JSON.stringify(def.hpArea.middleArea)},`)
      }
      if (def.hpArea.smallArea) {
        lines.push(`      smallArea: ${JSON.stringify(def.hpArea.smallArea)},`)
      }
      lines.push(`    },`)
    }
    lines.push(`  },`)
  }

  lines.push(`}`)
  lines.push(``)
  return lines.join('\n')
}

function main() {
  const middleJson = readJson(MIDDLE_AREA_JSON)
  const smallJson = readJson(SMALL_AREA_JSON)

  const middleAreas = safeArray(extractMiddleAreas(middleJson))
  const smallAreas = safeArray(extractSmallAreas(smallJson))

  if (middleAreas.length === 0) {
    throw new Error(`middle areas could not be parsed from: ${MIDDLE_AREA_JSON}`)
  }
  if (smallAreas.length === 0) {
    throw new Error(`small areas could not be parsed from: ${SMALL_AREA_JSON}`)
  }

  const middleAreaMap = buildMiddleAreaMap(middleAreas)

  let defsMap = buildDefsFromSmallAreas(middleAreaMap, smallAreas)
  defsMap = applyManualOverrides(defsMap)

  const defs = [...defsMap.values()].sort((a, b) =>
    a.canonical.localeCompare(b.canonical, 'ja')
  )

  const ts = toTsObject(defs)
  fs.mkdirSync(path.dirname(OUTPUT_TS), { recursive: true })
  fs.writeFileSync(OUTPUT_TS, ts, 'utf8')

  const summary = defs.map((d) => ({
    station: d.canonical,
    source: d.source,
    middleArea: d.hpArea?.middleArea ?? null,
    smallArea: d.hpArea?.smallArea ?? null,
    aliases: d.aliases.join(' / '),
  }))

  console.log(`Generated: ${OUTPUT_TS}`)
  console.log(`Total stations: ${defs.length}`)
  console.table(summary.slice(0, 50))
}

main()